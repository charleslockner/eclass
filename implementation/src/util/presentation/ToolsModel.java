package util.presentation;

/**
 * This class is responsible for handling functions related to the tools menu
 * @author Charles Lockner (clockner@calpoly.edu)
 */
public abstract class ToolsModel {
   
   /**
    * This is the corresponding function to launchRoster.
    * */
	abstract void modelLaunchRoster();
	
	/**
	 * This is the corresponding function to launchLayerPanel.
	 * */
	abstract void modelLaunchLayerPanel();
	
	/**
	 * This is the corresponding function to launchDrawingPanel.
	 * */
	abstract void modelLaunchDrawingPanel();
	
	/**
	 * This is the corresponding function to launchChatPanel.
	 * */
	abstract void modelLaunchChatPanel();
	
	/**
	 * This is the corresponding function to launchToolBox.
	 * */
	abstract void modelLaunchToolBox();
	
	/**
	 * This is the corresponding function to launchSlideControlPanel.
	 * */
	abstract void modelLaunchSlideControlPanel();
	
}
